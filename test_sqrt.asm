# Вектор из 7 элементов, например: [25, 36, 49, 64, 81, 100, 121]
# Регистр 1 будет хранить индекс для каждого элемента вектора.
# Регистр 2 будет использоваться для хранения результата (результаты записываются в новый вектор).

# Загрузим исходные данные в память (7 чисел).
load_constant 26 25 1   # Загрузить 25 в память по адресу 1
load_constant 26 36 2   # Загрузить 36 в память по адресу 2
load_constant 26 49 3   # Загрузить 49 в память по адресу 3
load_constant 26 64 4   # Загрузить 64 в память по адресу 4
load_constant 26 81 5   # Загрузить 81 в память по адресу 5
load_constant 26 100 6  # Загрузить 100 в память по адресу 6
load_constant 26 121 7  # Загрузить 121 в память по адресу 7

# Выполним поэлементно операцию sqrt() над каждым элементом вектора.
sqrt 24 1 8  # sqrt(25) -> 5, сохранить в память по адресу 8
sqrt 24 2 9  # sqrt(36) -> 6, сохранить в память по адресу 9
sqrt 24 3 10 # sqrt(49) -> 7, сохранить в память по адресу 10
sqrt 24 4 11 # sqrt(64) -> 8, сохранить в память по адресу 11
sqrt 24 5 12 # sqrt(81) -> 9, сохранить в память по адресу 12
sqrt 24 6 13 # sqrt(100) -> 10, сохранить в память по адресу 13
sqrt 24 7 14 # sqrt(121) -> 11, сохранить в память по адресу 14
